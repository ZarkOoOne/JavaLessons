class Absolute {

    int abs (int value){
        return value<0 ? -value : value;
    }

    long abs (long value) {
        return value<0 ? -value : value;
    }

    float abs (float value) {
        return value<0 ? -value : value;
    }

    double abs (double value) {
        return value<0 ? -value : value;
    }
}
