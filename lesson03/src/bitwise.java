public class bitwise {
    public static void main(String[] args) {
        System.out.println(43&103);
        System.out.println(43|103);
        System.out.println(~43);
        System.out.println(43^103);
        System.out.println(5>>1);
        System.out.println(5<<1);
        System.out.println(-1>>1);
        System.out.println(-1>>>1);
    }
}
